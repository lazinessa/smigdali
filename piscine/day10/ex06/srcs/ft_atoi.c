int		ft_atoi(char *str)
{
	int r;
	int sign;

	sign = 1;
	r = 0;
	while (*str == '\n' || *str == '\t' || *str == '\v' || *str == '\r' ||
	*str == '\f' || *str == ' ')
		str++;
	if ((*str == '+') || (*str == '-'))
	{
		if (*str == '-')
			sign = sign * (-1);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		r = r * 10 + (*str - '0');
		str++;
	}
	return (r * sign);
	return (0);
}
